    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String SearchCategory = JOptionPane.showInputDialog(null, "Please enter the Book Category you want to search");
        
        for(int i = 0; i < aList.size();) {
            if(SearchCategory.equalsIgnoreCase(aList.get(i).getBookCategory())) {
                JOptionPane.showMessageDialog(null, "Student ID:" +aList.get(i).getStudent()
                        + "\n Book Name:"+aList.get(i).getBookName()+ "\n Book Category:"+aList.get(i).getBookCategory());
            }
        
        
        }
        
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReturnActionPerformed
        // TODO add your handling code here:
        if(txtStudent.getText().equals("")||txtBookName.getText().equals("")|| 
                txtCategory.getText().equals("")){
            JOptionPane.showMessageDialog(null, "You must enter your Student ID, Book Name and Category to delete");
        }
        else if(count ==0){
            JOptionPane.showMessageDialog(null, "Sorry there are no Books to delete ath this time");
        }
        else{
            Student = txtStudent.getText();
            BookName = txtBookName.getText();
            BookCategory = txtCategory.getText();
            
            for(int i = 0; i < aList.size(); i++){
                if(aList.get(i).getStudent().equals(Student)&&aList.get(i).getBookName().equals(BookName)
                        &&aList.get(i).getBookCategory().equals(BookCategory)) {
                    aList.remove(i);
                    count = count - 1;
                    JOptionPane.showMessageDialog(null, "Your booking has been cancelled"+i);
                    break;
                }
                if(i==count-1){
                   JOptionPane.showMessageDialog(null,"Sorry that book could not be found"); 
                }
              }
          }
    }//GEN-LAST:event_btnReturnActionPerformed

